/* create a new function named isLowerCased with one parameter word
   create an if statement with a codition
   if condition is met return true
   else return false
   close if statement and function code block */
function isLowerCased(word) {
  if (word === word.toLowerCase()) {
    return true;
  } else {
    return false;
  }
}
